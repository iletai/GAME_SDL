#include "BaseObject.h"

////////////////////////////////////////////////////////////////////////////////////////////

BaseObject::BaseObject() : p_object(NULL), rect{ 0,0 }
{
}

////////////////////////////////////////////////////////////////////////////////////////////

BaseObject::~BaseObject()
{
	if (p_object != NULL)
	{
		SDL_FreeSurface(p_object);
	}
}

////////////////////////////////////////////////////////////////////////////////////////////

void BaseObject::ShowObject(SDL_Surface *des)
{
	if (p_object != NULL)
	{
		SDLCommonFunc::ApplySurface(p_object, des, rect.x, rect.y);
	}
}

////////////////////////////////////////////////////////////////////////////////////////////

bool BaseObject::LoadImageByPath(const char *file_namePath)
{
	p_object = SDLCommonFunc::LoadImageByPath(file_namePath);
	if (p_object == NULL)
		return false;
	return true;
}

////////////////////////////////////////////////////////////////////////////////////////////

void BaseObject::SetRectObject(const int& x, const int& y)
{
	rect.x = x;
	rect.y = y;
}

////////////////////////////////////////////////////////////////////////////////////////////

SDL_Rect	BaseObject::GetRectObject()
{
	return rect;
}

////////////////////////////////////////////////////////////////////////////////////////////

SDL_Surface* BaseObject::GetObject()
{
	return p_object;
}

